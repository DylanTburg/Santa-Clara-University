% *************************************************************************
% clear work space and command window
% *************************************************************************
clear
clc

% *************************************************************************
% variables
% *************************************************************************
Vs  = 144;
Is2 = 3;

R1  = 10;
R2  = 4;
R3  = 80;
R4  = 5;

% *************************************************************************
% set symbolic symbols
% *************************************************************************
syms Vb Vc Vx real

% *************************************************************************
% system of equations
% *************************************************************************
eq1 = (7+Vb/3+(Vb-Vc)) == 0;
eq2 = -2*Vx + Vc-Vb + (Vc-4)/2 == 0;
eq3 = Vx == Vc-4;

% *************************************************************************
% set equations and variables; Note: the number of equations
% and the number of unknown variables should be the same; the
% order to unknown variables should be reserved: V1, V2, ...
% *************************************************************************
eqns = [eq1, eq2, eq3];
vars = [Vb, Vx, Vc];

% *************************************************************************
% solve for systems of linear equations
% *************************************************************************
Results = solve(eqns, vars);

% *************************************************************************
% converting symbolic value to real values
% *************************************************************************
Vb = double(Results.Vb)
Vc = double(Results.Vc)
Vx = double(Results.Vx)


